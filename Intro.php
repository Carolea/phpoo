 * autant que nous le souhaitons afin d'obtenir une infinité d'objets.
 * 
 * Vocabulaire: en programmation objet, nous ne parlerons plus 
 * fonctions mais méthode, nous ne parlerons plus de variables mais
 * d'attributs et de propriétés.
 * 
 * Une classe est une entité regroupant un certain nombre d'attributs
 * et de méthodes que tout objet issu de cette classe possédera.
 * 
 * Différence entre une classe et un objet
 * Exemple: plan de construction d'une voiture
 * classe c'est le plan objet la voiture.
 * 
 * Définition: 
 * Une instance de classe c'est le fait de créer un objet 
 * de cette classe
